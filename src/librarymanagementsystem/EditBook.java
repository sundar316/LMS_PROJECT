    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        String bookID=jTextField1.getText();
        try
        {
            if(jTextField1.getText().trim().isEmpty())
            {
                JOptionPane.showMessageDialog(null, "Enter the Book ID");
            }
            else
            {
            Connection con=ConnectionProvider.getCon();
            Statement st=con.createStatement();
            ResultSet rs=st.executeQuery("select *from Books where bookID='"+bookID+"'");
            if(rs.next())
            {
                
                jTextField3.setText(rs.getString(5));
                
                jTextField4.setText(rs.getString(2));
            }
            else
            {
                JOptionPane.showMessageDialog(null, "Incorrect Book ID");
                dispose();
                new EditBook().setVisible(true);
            }
            }
        }
        catch(HeadlessException | SQLException e)
        {
            JOptionPane.showMessageDialog(null, "Connection Error");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

