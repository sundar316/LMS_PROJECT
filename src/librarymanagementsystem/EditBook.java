    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String Operation=(String)jComboBox1.getSelectedItem();
        int noOfBooks=Integer.parseInt(jTextField2.getText());
        String bookID=jTextField1.getText();
        try
        {
            if(jTextField2.getText().trim().isEmpty())
            {
                JOptionPane.showMessageDialog(null, "Enter the No. of Books");
            }
            else
            {
            Connection con=ConnectionProvider.getCon();
            Statement st=con.createStatement();
            ResultSet rs=st.executeQuery("select *from Books where bookID='"+bookID+"'");
            rs.next();
            int no_of_books=rs.getInt(5);
            int No_Of_Books=rs.getInt(6);
            if(Operation.equals("Add"))
            {
            no_of_books =no_of_books+noOfBooks;
            No_Of_Books=No_Of_Books+noOfBooks;
            st.executeUpdate("Update Books set noOfBooks='"+no_of_books+"' , availableBooks='"+No_Of_Books+"' where bookID='"+bookID+"'");
            JOptionPane.showMessageDialog(null, "Successfully updated");
            dispose();
            new EditBook().setVisible(true);
            }
            else{
                no_of_books =no_of_books-noOfBooks;
                No_Of_Books=No_Of_Books-noOfBooks;
                st.executeUpdate("Update Books set noOfBooks='"+no_of_books+"', availableBooks='"+No_Of_Books+"' where bookID='"+bookID+"'");
                JOptionPane.showMessageDialog(null, "Successfully updated");
                dispose();
                new EditBook().setVisible(true);
            }
            }
        }
        catch(Exception e)
                {
                    JOptionPane.showMessageDialog(null, e);
                }
        
    }//GEN-LAST:event_jButton1ActionPerformed

